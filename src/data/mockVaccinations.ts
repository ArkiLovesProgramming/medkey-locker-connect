// Helper functions
export function getVaccinationsByPatient(patientId: string): VaccinationRecord[] {
  return vaccinationRecords.filter(v => v.patientId === patientId);
}

export function getVaccinationsByStatus(status: string): VaccinationRecord[] {
  return vaccinationRecords.filter(v => v.status === status);
}

export function getVaccinationById(id: string): VaccinationRecord | undefined {
  return vaccinationRecords.find(v => v.id === id);
}

export function getDueVaccinations(): VaccinationRecord[] {
  return vaccinationRecords.filter(v => v.status === 'due' || v.status === 'overdue');
}

export function getCompletedVaccinations(): VaccinationRecord[] {
  return vaccinationRecords.filter(v => v.status === 'completed');
}

export function getVaccinationsByDateRange(start: string, end: string): VaccinationRecord[] {
  return vaccinationRecords.filter(v => {
    if (!v.dateAdministered) return false;
    const vacDate = new Date(v.dateAdministered);
    return vacDate >= new Date(start) && vacDate <= new Date(end);
  });
}

export function searchVaccinations(query: string): VaccinationRecord[] {
  const lowerQuery = query.toLowerCase();
  return vaccinationRecords.filter(v => 
    v.vaccineName.toLowerCase().includes(lowerQuery) ||
    v.patientName.toLowerCase().includes(lowerQuery) ||
    v.manufacturer?.toLowerCase().includes(lowerQuery)
  );
}

export function getVaccinationSummary(patientId: string): Record<string, any> {
  const patientVaccines = vaccinationRecords.filter(v => v.patientId === patientId && v.status === 'completed');
  const dueVaccines = vaccinationRecords.filter(v => v.patientId === patientId && (v.status === 'due' || v.status === 'overdue'));
  
  return {
    patientId,
    totalVaccinations: patientVaccines.length,
    dueVaccinations: dueVaccines.length,
    lastFluShot: patientVaccines.find(v => v.vaccineName.includes('Influenza'))?.dateAdministered,
    lastCOVIDBooster: patientVaccines.find(v => v.vaccineName.includes('COVID'))?.dateAdministered,
    tetanusDue: patientVaccines.find(v => v.vaccineName.includes('Tdap') || v.vaccineName.includes('Td'))?.dueDate,
  };
}
