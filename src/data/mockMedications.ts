// Helper functions
export function getMedicationById(id: string): Medication | undefined {
  return medicationDatabase.find(m => m.id === id);
}

export function getActiveMedicationsByMember(memberId: string): ActiveMedication[] {
  return activeMedications.filter(m => m.memberId === memberId);
}

export function searchMedications(query: string): Medication[] {
  const lowerQuery = query.toLowerCase();
  return medicationDatabase.filter(m => 
    m.name.toLowerCase().includes(lowerQuery) ||
    m.genericName?.toLowerCase().includes(lowerQuery) ||
    m.category.toLowerCase().includes(lowerQuery)
  );
}
